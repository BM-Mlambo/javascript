//Operators are symbols used to perform operations on values or variables
//Arithmetic Operators-Use to perform mathematical calculations
let a=10
let b=3
console.log(a+b)
console.log(a-b)
console.log(a*b)
console.log(a/b)
console.log(a%b)//Remainder
console.log(a**b)//power of
//Comparison Operators-They are used to compare two values
//they always return true or false
let x=5
let y=8
console.log(x>y)//greater than
console.log(x<y)//Less than
console.log(x>=y)//greater than or equal to
console.log(x<=y)//Less than or equal to
console.log(x==y)//Equal to
console.log(x!=y)//Not equal to
//logical operators-they are used to combine multiple conditions
//The And Operators(&&)-both conditions must be true
let age=20
let hasId=false
console.log(age >=18 && hasId)
//The OR operators(||)-Only one condition needs to be true
let studentAge=15
let hasParentpermission=true
console.log(studentAge>=18 || hasParentpermission)
//Increament and Decrement-these operators icrease or decrese a value by one
let num=15
num++
console.log(num)
//decrement
let j=13
j--
console.log(j)
//String operators-we use the plus operator to combine strings
let firstName="Brian"
let lastName="Mlambo"
let fullName=firstName+" "+lastName
console.log(fullName)