let fruits=["apple","banana","mangoes"]
console.log(fruits)
//. push-adds a new item at the end of array
fruits.push("orange")
console.log(fruits)
//.unshift-adds a new item at the begining of the list
fruits.unshift("pineapple")
console.log(fruits)
//.pop()-remove the last item from the array
fruits.pop()
console.log(fruits)
//.shift()-remove the first item from array
fruits.shift()
console.log(fruits)
//.includes()-check if an array contains a value.
console.log(fruits.includes("apple"))
console.log(fruits.includes("pineaple"))
//.length-returns the number in array
console.log(fruits.length)